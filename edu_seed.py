def send(text, thread_id):
    api = f"https://api.telegram.org/bot{BOT_TOKEN}/sendMessage"
    payload = {"chat_id": CHAT_ID, "text": text, "parse_mode": "HTML",
               "message_thread_id": thread_id, "disable_web_page_preview": True}
    data = urllib.parse.urlencode(payload).encode()
    try:
        with urllib.request.urlopen(urllib.request.Request(api, data=data), timeout=25) as r:
            resp = json.loads(r.read())
        if not resp.get("ok"):
            print("TG ERROR:", resp)
            return False
        return True
    except urllib.error.HTTPError as e:
        print("TG HTTP", e.code, e.read().decode("utf-8", "replace")[:200])
        return False
    except Exception as e:
        print("TG FAIL:", e)
        return False


def main():
    if not BOT_TOKEN or not CHAT_ID:
        print("Missing BOT_TOKEN/CHAT_ID"); sys.exit(1)
    sent = 0
    for p in RULES_POSTS:
        if send(p, ROOM_RULES):
            sent += 1
            print(f"📌 Правила: пост {sent} OK")
        time.sleep(2)
    for p in HELP_POSTS:
        if send(p, ROOM_HELP):
            sent += 1
            print(f"🆘 Помощ: пост OK")
        time.sleep(2)
    print(f"Общо {sent} обучителни поста.")


if __name__ == "__main__":
    main()
